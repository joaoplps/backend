public class Pessoa {
    private String sNome;
    private int iIdade;
    private boolean bSexo;
    private String sMatricula;

    public String getsNome() {
        return sNome;
    }

    public void setsNome(String sNome) {
        this.sNome = sNome;
    }

    public int getiIdade() {
        return iIdade;
    }

    public void setiIdade(int iIdade) {
        this.iIdade = iIdade;
    }

    public boolean isbSexo() {
        return bSexo;
    }

    public void setbSexo(boolean bSexo) {
        this.bSexo = bSexo;
    }

    public String getsMatricula() {
        return sMatricula;
    }

    public void setsMatricula(String sMatricula) {
        this.sMatricula = sMatricula;
    }
    
    
}
